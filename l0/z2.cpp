#include <iostream>

using namespace std;
int main()
{
	int x=2;
	int y=3;
	int z=5;

	cout << "x+y= " << x+y << endl;
	cout << "y-x= " << y-x << endl;
	cout << "z/x= " << z/x << endl;
	cout << "z%x= " << z%x << endl;

	cout << "z*x+y= " << z*x+y << endl;
	cout << "z*(x+y)= " << z*(x+y) << endl;

	cout << "z+(x+y)= " << z+(x+y) << endl;
	cout << "(z+x)+y= " << (z+x)+y << endl;

	return 0;
}
